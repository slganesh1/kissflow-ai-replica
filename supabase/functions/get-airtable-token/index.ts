import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  'Access-Control-Allow-Origin': '*',
  'Access-Control-Allow-Headers': 'authorization, x-client-info, apikey, content-type',
};

serve(async (req) => {
  // Handle CORS preflight requests
  if (req.method === 'OPTIONS') {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    // Get Airtable personal access token from Supabase secrets
    const airtableToken = Deno.env.get('AIRTABLE_PERSONAL_ACCESS_TOKEN');
    
    if (!airtableToken) {
      console.error('AIRTABLE_PERSONAL_ACCESS_TOKEN not found in environment');
      return new Response(
        JSON.stringify({ error: 'Airtable personal access token not configured' }),
        { 
          status: 500, 
          headers: { ...corsHeaders, 'Content-Type': 'application/json' } 
        }
      );
    }

    console.log('Airtable token retrieved successfully');

    return new Response(
      JSON.stringify({ 
        success: true, 
        token: airtableToken 
      }),
      { 
        status: 200, 
        headers: { ...corsHeaders, 'Content-Type': 'application/json' } 
      }
    );

  } catch (error) {
    console.error('Error in get-airtable-token function:', error);
    return new Response(
      JSON.stringify({ 
        error: 'Internal server error', 
        details: error.message 
      }),
      { 
        status: 500, 
        headers: { ...corsHeaders, 'Content-Type': 'application/json' } 
      }
    );
  }
});